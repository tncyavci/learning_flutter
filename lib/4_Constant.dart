// final and const
// const: variable is created in memory as soon as we create it
// final: occurs when the code is executed after the variable is created
// const: not used in class
// constant use improves memory
// final usage better than const
void main(){
  final pi = 3.14;
  final String message = "Hello!";
  const y = 20;
  const double a = 1.2;

}